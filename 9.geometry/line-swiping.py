points = [(1, 5), (3, 9), (7, 8), (10, 3), (6, 1)]

# x_value를 기준으로 오름차순 정렬
points.sort()

# 변수 초기화
area = 0
prev_x, prev_y = points[0]

# 스위핑 시작
for x, y in points:
    # 직사각형 넓이 합산
    area += (x - prev_x) * y

    # 이전 좌표 갱신
    prev_x, prev_y

print(area)

# 라인 스위핑(line sweep) 알고리즘은 2차원 평면 좌표계 위의 문제를 효율적으로 해결하기 위해 사용됩니다. 이는 일반적으로 선을 직선(수직 또는 수평)으로 스캔하면서 무언가를 찾는 형태로 작동합니다. 여기서 "선"이란 이 문제에서 필요한 사각형, 다각형, 점 등을 의미합니다.

# Python으로 라인 스위핑 알고리즘을 구현하는 방법은 매우 다양하지만, 여기에 간단한 예시 코드를 제공하겠습니다. 본 코드는 2차원 평면 상의 점들이 주어지고, 각 점들과 x-y 축이 이루는 직사각형 면적을 구하는 알고리즘입니다.

# 위 코드에서는 points 리스트에 2차원 좌표 값들을 저장한 뒤, x 값을 기준으로 정렬합니다. 이후 points를 점진적으로 탐색하면서, 이전 좌표와 현재 좌표 사이의 직사각형 면적을 구해 area에 더해나가는 방식으로 문제를 해결합니다.